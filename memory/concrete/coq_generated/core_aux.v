    match ( (pat, cval)) with | (CaseBase (None,  _),  _) =>
      (* e[_ \ v] = e *) env1 | (CaseBase (Some sym1,  _),  _) =>
      (* e[sym \ v] *) fmap_add sym1 cval env1
