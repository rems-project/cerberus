    union_saves st
      (* begin block *)
      union_saves
        {[ st1 with tmp_acc:= fmap_map
                                (fun (p : (list (symbol.sym )*generic_expr a (unit ) (symbol.sym )) % type) =>
                                   match ( (p) ) with ( (syms,  e)) =>
                                     (syms, Expr annots1 (Esseq pat e e2))
